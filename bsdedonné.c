void tri(int tab[], int debut, int fin) {
    int p, i, j;
    if(debut < fin) {
        p = debut;
        i = debut;
        j = fin;
        while (i < j) {
            while(tab[i] <= tab[p] && i < fin)
                i++;
            while(tab[j] > tab[p])
                j--;
            if(i < j) {
                changer(&tab[i], &tab[j]);
            }
        }
        changer(&tab[p], &tab[j]);
        tri(tab, debut, j - 1);
        tri(tab, j + 1, fin);
    }
}

int dichotomie(int x, int tab[], int debut, int fin){
    int milieu = (debut + fin)/2;
    if(debut<= fin){
        if(tab[milieu]==x){
            return milieu;
        }
        else if(tab[milieu]<x){
            return dichotomie(x, tab, milieu+1, fin);
        }
        else{
            return dichotomie(x, tab, milieu-1, fin);
        }
    }
    return -1;
}